def solve_36fdfd69(I):
    x1 = upscale(I, 2)
    x2 = objects(x1, True, True, True)
    x3 = colorfilter(x2, 2)
    x4 = fork(manhattan, first, last)
    x5 = lbind(greater, 5)
    x6 = compose(x5, x4)
    x7 = product(x3, x3)
    x8 = sfilter(x7, x6)
